export function generateRandomColor(n) {
  let colors = [];
  for (let i = 0; i < n; i++) {
    const red = Math.floor(Math.random() * 256);
    const green = Math.floor(Math.random() * 256);
    const blue = Math.floor(Math.random() * 256);
    let color = `rgb(${red} ${green} ${blue} / 40%)`;
    colors.push(color);
  }

  return colors;
}

